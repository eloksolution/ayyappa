package in.eloksolutions.ayyappa.vo;

public class UserPadis {
	String userId;
	String userName;
	String padoId;
	String padiName;
	public String getUserId() {
		return userId;
	}
	public String getUserName() {
		return userName;
	}
	public String getPadoId() {
		return padoId;
	}
	public String getPadiName() {
		return padiName;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public void setPadoId(String padoId) {
		this.padoId = padoId;
	}
	public void setPadiName(String padiName) {
		this.padiName = padiName;
	}
	
	
}
